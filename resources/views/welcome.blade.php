@section('script')
    <script>
        $().ready(() => {

            $( window ).scroll((() => {
                const scrollTop = Math.max(window.pageYOffset, document.documentElement.scrollTop, document.body.scrollTop);
                // console.log('top: ' + $('html, body').scrollTop());
                // console.log('top: ' + scrollTop);
                if (scrollTop > 300) {
                    $('.btn-translucent').show();
                } else {
                    $('.btn-translucent').hide();
                    $('#mySidenav').width('0vw');
                }
            }));

            $('body').click(function(evt) {
                if(evt.target.id != "mySidenav"
                && evt.target.id != 'menuBtn'
                && evt.target.classList != 'fas fa-bars') {
                    console.log(evt.target);
                    $('#mySidenav').width('0vw');
                }
            });

            $('.navigation').on('click', function() {
                const href = $(this).attr('href');
                const top = $(href).position().top;

                $('html, body').animate({
                    scrollTop: top
                }, 900);
            });

            $('#toTop').on('click', function() {

                $('html, body').animate({
                    scrollTop: 0
                }, 900);
            });

            $('#menuBtn').on('click', function() {
                const navWidth = $('#mySidenav').width();
                if(navWidth === 0) {
                    $('#mySidenav').width('45vw');
                } else {
                    $('#mySidenav').width('0vw');
                }
            });
        });
    </script>
